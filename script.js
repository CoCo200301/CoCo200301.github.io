// Función para mostrar el letrero
function mostrarLetrero(letreroId) {
    var letrero = document.getElementById(letreroId);
    letrero.style.display = "block";
}

// Función para cerrar el letrero
function cerrarLetrero(letreroId) {
    var letrero = document.getElementById(letreroId);
    letrero.style.display = "none";
}

